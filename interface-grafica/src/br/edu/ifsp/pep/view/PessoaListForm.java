    private void BExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BExcluirActionPerformed
        int linha = TabelaAtributos.getSelectedRow();
        if (linha != -1) {

            try {
                pessoaDaoJDBC.remover(pessoas.get(linha).getCodigo());
                JOptionPane.showMessageDialog(rootPane, "Deletado com sucesso");
            } catch (Exception ex) {
                Logger.getLogger(PessoaListForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        pessoas = pessoaDaoJDBC.getTodos();
        atualizarTabela();
    }//GEN-LAST:event_BExcluirActionPerformed

    private void BPesquisar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPesquisar1ActionPerformed
        // TODO add your handling code here:
        
        if ("".equals(JPesquisar.getText()))
            pessoas = pessoaDaoJDBC.getTodos();
        else
            pessoas = pessoaDaoJDBC.consultarPeloNome(JPesquisar.getText());
        
        atualizarTabela();
        
    }//GEN-LAST:event_BPesquisar1ActionPerformed

